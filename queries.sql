#Monthly Sales Trend
select date_format(o.order_date, '%Y-%m') as month,
sum(od.quantity * p.price) as revenue
from orders o
join orderdetails od on o.order_id = od.order_id
join products p on od.product_id = p.product_id
group by month
order by month;

