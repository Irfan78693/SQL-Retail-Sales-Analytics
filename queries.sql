#Display all orders with customer names.
select order_id, customer_name
from orders o
join customers c on o.customer_id = c.customer_id;

#Show product names and quantities for each order.
select o.order_id, p.product_name, o.quantity
from orderdetails o
join products p on o.product_id = p.product_id;

#Display employee name who handled each order.
select o.order_id, e.employee_name
from orders o
join employees e on o.employee_id = e.employee_id;

#Show all customers and their orders (including customers without orders).
select customer_name,order_id from customers c
left join orders o on c.customer_id = o.customer_id;

#Display total number of orders handled by each employee.
select employee_name,count(order_id) as total_orders_handled 
from orders o
join employees e on o.employee_id =e.employee_id
group by employee_name;


#Find customers who placed more than 1 order.
select customer_name
from customers c
where customer_id in (select customer_id from orders group by customer_id having count(order_id)>1);

#Display products with price higher than the average product price.
select product_name,price from products p
where price>(select avg(price) from products);

#Find the customer who placed the highest number of orders.
select c.customer_name
from customers c
where c.customer_id = (select customer_id from orders
group by customer_id
order by COUNT(order_id) desc
limit 1);

#Show orders where total quantity is greater than the average quantity.
select order_id from orderdetails
group by order_id
having SUM(quantity) > (select avg(quantity) from orderdetails);

#Find employees who handled more orders than the average employee
select employee_id 
from orders 
group by employee_id 
having COUNT(order_id) > (select avg(order_count) 
from (select COUNT(*) as order_count
from orders group by employee_id) as o);


#Using CTE, calculate total sales amount for each order.
with sales_cte as(select od.order_id,sum(od.quantity * p.price) as total_sales
from orderdetails od 
join products p on od.product_id = p.product_id
group by order_id)
select * from sales_cte order by total_sales desc;

#Using CTE, find customers whose total purchase amount is greater than 20,000.
with purchase_cte as
(select customer_name,sum(quantity*price) as total_purchase
from orderdetails od 
join products p on od.product_id = p.product_id
join orders o on od.order_id = o.order_id
join customers c on o.customer_id = c.customer_id
group by customer_name)
select * from purchase_cte where total_purchase >20000;

#Using CTE, display the total revenue generated by each product category.
with category_cte as(
select p.category,sum(od.quantity * p.price) as total_revenue
from products p
join orderdetails od on p.product_id = od.product_id
group by p.category)
select * from category_cte;

#Using CTE, find the top 3 customers based on total spending.
with spend_cte as
(select customer_name,sum(quantity*price) as total_spend
from orderdetails od 
join products p on od.product_id = p.product_id
join orders o on od.order_id = o.order_id
join customers c on o.customer_id = c.customer_id
group by customer_name)
select * from spend_cte 
order by total_spend desc
limit 3;

#Assign rank to customers based on total purchase amount.
with rank_cte as(select customer_name,sum(quantity*price) as total_purchase
from orderdetails od 
join products p on od.product_id = p.product_id
join orders o on od.order_id = o.order_id
join customers c on o.customer_id = c.customer_id
group by customer_name)
select *,rank() over(order by total_purchase desc) as row_rank from rank_cte;

#Display cumulative sales amount by order date.
